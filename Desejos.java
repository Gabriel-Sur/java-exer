public class Desejos {


    String nome;
    Desenvolvedora desenvolvedora;
    Distribuidora distribuidora;
    int preco;
    int idadeMinima;


    public Desejos(String nome, Desenvolvedora desenvolvedora, Distribuidora distribuidora, int preco, int idadeMinima){
        this.nome = nome;
        this.desenvolvedora = desenvolvedora;
        this.distribuidora = distribuidora;
        this.preco = preco;
        this.idadeMinima = idadeMinima;
    }




}
